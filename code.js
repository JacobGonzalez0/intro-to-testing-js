function sayHello(input){
    if(input === "Pat"){
        return "Hello, Pat!"
    }else if(input === "Alex"){
        return "Hello, Alex!"
    }else if(input === "Jenny"){
        return "Hello, Jenny!"
    }else{
        return ""
    }
}